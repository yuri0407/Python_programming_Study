import turtle as t

t.shape('turtle');
t.speed(0)
t.pensize(20)

t.circle(100)

t.up()
t.fd(240)
t.down()
t.pencolor('red')
t.circle(100)

t.up()
t.backward(480)
t.down()
t.pencolor('blue')
t.circle(100)

t.up()
t.rt(90)
t.fd(120)
t.lt(90)
t.fd(120)
t.down()
t.pencolor('yellow')
t.circle(100)

t.up()
t.fd(240)
t.down()
t.pencolor('green')
t.circle(100)


t.done()